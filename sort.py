import numpy as np   #int
a=np.array([121,4,5,3,8,9,0,1])
print(np.sort(a))


#string
a = np.array(['banana', 'cherry', 'apple','Apple'])
print(np.sort(a))


#boolean
a= np.array([True, False, True,False])
print(np.sort(a))


# Sorting a 2-D Array
a= np.array([[3, 2, 4], [5, 0, 1]])
print(np.sort(a))

