import numpy as np    #addition(+)
a=np.array([10,20,30])
b=np.array([1,2,3])
print(a+b)   #O/P: [11 22 33]

import numpy as np  #subraction(-)
a=np.array([10,20,30])
b=np.array([1,2,3])
print(a-b)   #O/P: [9 18 27]

import numpy as np  #multiply(*)
a=np.array([10,20,30])
b=np.array([1,2,3])
print(a*b)   #O/P: [10 20 30]

import numpy as np    #division(/)
a=np.array([10,20,30])
b=np.array([2,2,5])
print(a/b)   #O/P: [ 5. 10.  6.]

import numpy as np  #floor division(//)
a=np.array([10,20,30])
b=np.array([2,2,5])
print(a/b)   #O/P: [ 5 10  6]


import numpy as np  #modulus(%)
a=np.array([10,20,30])
